      <h1>Gestion des incidents</h1>
      <h2>Définition</h2>
      <p>Un incident est un événement qui perturbe le fonctionnement normal du service, impactant les utilisateurs ou les opérations.</p>
      <h2>Processus de gestion</h2>
      <ul>
        <li><strong>Identification :</strong> Détection via monitoring, alertes ou signalements utilisateurs.</li>
        <li><strong>Classification :</strong> Priorisation selon l'impact et l'urgence.</li>
        <li><strong>Enquête :</strong> Analyse des causes racines et collecte de données.</li>
        <li><strong>Résolution :</strong> Mise en place de correctifs temporaires ou définitifs.</li>
        <li><strong>Communication :</strong> Information des parties prenantes et mise à jour du statut.</li>
        <li><strong>Clôture :</strong> Documentation de l'incident et des actions prises.</li>
      </ul>
      <h2>Outils</h2>
      <ul>
        <li>Système de ticketing (ex: Jira, GitHub Issues)</li>
        <li>Monitoring (ex: Grafana, Prometheus)</li>
        <li>Alertes (ex: PagerDuty, Opsgenie)</li>
      </ul>
      <h2>Métriques</h2>
      <ul>
        <li>Taux de résolution dans les SLA</li>
        <li>Délai moyen de résolution (MTTR)</li>
        <li>Taux de récurrence des incidents</li>
      </ul>
